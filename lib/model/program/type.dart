enum ProgramType {
  /// 综合节目，包含不止一个分类的节目
  integrate,

  /// 新闻节目，聚焦时事与政治的新闻类节目
  news,

  /// 音乐节目，纯粹播放音乐或对音乐进行点评的节目
  music,

  /// 播客节目，邀请嘉宾进行深度对话或者话题延申的节目
  podcast,

  /// 综艺节目，交流趣事讨论八卦或者主持人持续与听众进行互动的节目
  entertainment,

  /// 体育节目，报道实况比赛或者分析赛点事件的节目
  sports,

  /// 广播剧节目，音频小说或评书节目
  storytelling,

  /// 教育节目，谈论家庭教育或孩子的心理问题或教育讲座节目
  educational,

  /// 财经节目，解析今日股市或提供投资建议的节目
  finance,

  /// 健康节目，与听众交流健身或养生知识的节目
  health,
}